QUIZ	START	0
	EXTREF INIT, FUNCA, FUNCB

FIRST	+JSUB INIT
	LDX =X'000000'
	+LDT #MAXLEN
ILOOP	LDCH STRIN, X
	+JSUB FUNCA
	TIXR T
	JLT ILOOP
	LDX =X'000000'
OLOOP	+JSUB FUNCB
	ADD #32
	+STCH STROUT, X
	TIXR T
	JLT OLOOP
HALT	J	HALT
	LTORG
STRIN	BYTE C'STRESSED'
MAXLEN	EQU 8
STROUT	RESB MAXLEN
	END QUIZ 